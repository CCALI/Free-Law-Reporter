                    <h2 class="c2 c4">Introducing The Free Law Reporter</h2>

                    <p class="c2 c5"></p>

                    <h3 class="c6 c2">What is the Free Law Reporter?</h3>

                    <p class="c2">The Free Law Reporter&#8482; (FLR) is an experiment that builds on<a href="http://radar.oreilly.com/2010/12/the-report-of-current-opinions.html">Carl Malamud&#8217;s Report of Current Opinions (RECOP)</a>.(more info about RECOP from<a href="http://onward.justia.com/2010/12/20/recop-the-report-of-current-opinions/">Justia</a>,<a href="http://www.lawsitesblog.com/2010/12/more-details-on-recop-from-fastcases-ed-walters.html">Robert Ambrogi</a> and <a href="http://www.prweb.com/releases/Fastcase-LawGov/Public-Law-RECOP/prweb4912854.htm">Ed Walters, CEO of Fastcase</a>). The RECOP bulk feed can be found at
                    <a href="http://bulk.resource.org/courts.gov/recop/">http://bulk.resource.org/courts.gov/recop/</a>.</p>

                    <p class="c2">The goal of FLR is to develop a freely available, unencumbered law reporter that is capable of serving as a resource for education, research, and practice. The first step is to use FLR to provide greater access through enhanced and alternate formats of the weekly feeds coming from RECOP.</p>

                    <p class="c2">The weekly feeds provided by RECOP are a new source of court opinions from across the country with each weekly feed containing all slip and final opinions primarily from the appellate courts of the 50 states and the federal government released under a <a href="http://creativecommons.org/publicdomain/zero/1.0/">Creative Commons Zero License</a> packaged as XML files in a single weekly archive.</p>

                    <p class="c2">Each weekly feed contains 2 groupings of opinions: slip opinions and paginated cases. The slip opinions are individual documents that come directly from the court, are paginated by the court, and contain little or no citation information. The paginated cases are individual documents that have been added to the West reporter system, contain West pagination and full West citations.</p>

                    <p class="c2">CALI&#8217;s FLR project is processing the slip opinion portion of the archives to create a body of court opinions that is accessible to anyone including educators, librarians, students, lawyers and the public. The raw XML of the archives is processed into valid XHTML and some meta data is added to facilitate the indexing and classification of the documents. The XHTML files are saved into a web folder structure organized by weekly RECOP volume and jurisdiction for viewing with a web browser. The XHTML files are fed into a search engine powered by <a href="http://lucene.apache.org/solr/">Apache Solr</a> allowing for sophisticated searching and analysis of the documents. Finally the weekly volumes are gathered by state and federal jurisdiction into ebooks in the .epub format viewable on virtually any desktop, laptop, tablet, smartphone and e-reader device. All of this available now
                    at <a href="http://www.freelawreporter.org">www.freelawreporter.org</a>. The FLR website is updated weekly as new RECOP archives are released.</p>

                    <p class="c2">This initial level of processing provides the FLR with a foundation upon which to grow. The next steps will depend upon community involvement. CALI is researching methods for working with the FLR that will allow the addition of headnotes and tags to opinions. &nbsp;CALI is also working on a service to allow users, (e.g. law faculty) to group and edit opinions and publish them as custom ebooks for legal education.</p>

                    <p class="c2 c5"></p>

                    <h3 class="c6 c2">What can I do with FLR?</h3>

                    <p class="c2">The first 2 features of FLR to be made available are search and ebooks. Basic keyword searching is available with facet searching and &#8220;more like this&#8221; functionality coming shortly. Because search is powered by Solr, more sophisticated search is possible and that functionality will be developed as the collection grows.</p>

                    <p class="c2">Each weekly archive file provides the content for the generation of some 60 ebooks every week. Each state and federal jurisdiction is gathered into a volume each week. For example <a href="http://www.freelawreporter.org/1/alaska/1FLRAlaska.epub">1FLRAlaska.epub</a> contains the opinions from the Alaska state courts that were included in the first RECOP archive file. The ebooks are in the .epub format, a widely supported format that can be read on Windows, Mac, and Linux PCs and laptops as well as iPad, iPhone, and Android devices. Amazon Kindle support is possible through third party conversion programs like <a href="http://calibre-ebook.com/">Calibre</a> while we research more direct paths to Kindle support. &nbsp;Access to the FLR ebook volumes will be available via the <a href="http://lec.cali.org/">CALI Legal
                    Education Commons</a> and <a href="http://www.freelawreporter.org">FLR</a> websites.</p>

                    <p class="c2 c5"></p>

                    <h3 class="c2 c6">What is in the FLR?</h3>

                    <p class="c2">The FLR contains whatever is included in the slip opinion archives of the RECOP. This includes primarily appellate level courts in 50 states and DC, the federal circuit courts, and SCOTUS. For the most part coverage begins with 1/1/2011 and continues forward. It is not clear how thorough the coverage of slip opinions is, so omissions may occur. The archives include everything that comes from a given court including &#8220;unpublished&#8221; opinions, orders, and motions. Since the FLR project&#8217;s handling of the data is automated, everything that is in the archive is being processed. This means that search results and ebooks may include a lot of material that is not terribly useful. We will refine the process going forward and decide on how and when to re-publish the growing back catalog as needs dictate. &nbsp;</p>

                    <p class="c2">Obviously, we are looking at how to expand FLR to cover public.resource.org&#8217;s previous releases of federal opinions as well as the paginated cases section of the current RECOP feeds. These materials will be added to the FLR search database to provide greater coverage, but it is not yet clear to us that ebook versions should be generated. &nbsp;</p>

                    <p class="c2 c5"></p>

